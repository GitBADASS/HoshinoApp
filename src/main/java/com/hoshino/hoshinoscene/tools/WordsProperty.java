package com.hoshino.hoshinoscene.tools;

public class WordsProperty {

    int type;

    String translation;

    String fixedTranslation;

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getTranslation() {
        return translation;
    }

    public void setTranslation(String translation) {
        this.translation = translation;
    }

    public String getFixedTranslation() {
        return fixedTranslation;
    }

    public void setFixedTranslation(String fixedTranslation) {
        this.fixedTranslation = fixedTranslation;
    }
}
